package edu.whu.clock.graphsearch.util;

public class SourceAndDistance {

	private int source;
	private double distance;
	
	public SourceAndDistance(int source, double distance) {
		super();
		this.source = source;
		this.distance = distance;
	}

	public int getSource() {
		return source;
	}

	public double getDistance() {
		return distance;
	}
	
}
